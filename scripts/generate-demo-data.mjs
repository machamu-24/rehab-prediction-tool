import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SNAPSHOT = path.join(ROOT, "scripts/demo-data.snapshot.json");
const OUTPUT = path.join(ROOT, "client/src/demo/staticData.generated.ts");

function readSnapshot() {
  if (!fs.existsSync(SNAPSHOT)) {
    throw new Error(
      `Missing ${path.relative(ROOT, SNAPSHOT)}. Run pnpm refresh:demo-data from a DB-backed local app.`,
    );
  }

  return JSON.parse(fs.readFileSync(SNAPSHOT, "utf8"));
}

function normalizeOutcome(outcome) {
  return {
    id: Number(outcome.id),
    name: outcome.name,
    description: outcome.description ?? null,
    unit: outcome.unit ?? null,
    positiveLabel: outcome.positiveLabel,
    negativeLabel: outcome.negativeLabel,
    isDefault: Boolean(outcome.isDefault),
    sortOrder: Number(outcome.sortOrder ?? 0),
    createdAt: outcome.createdAt,
    updatedAt: outcome.updatedAt,
  };
}

function normalizeRule(rule) {
  return {
    id: Number(rule.id),
    outcomeId: Number(rule.outcomeId),
    name: rule.name,
    ruleType: rule.ruleType,
    source: rule.source,
    sourceUrl: rule.sourceUrl || null,
    evidenceLevel: rule.evidenceLevel,
    applyConditions: Array.isArray(rule.applyConditions) ? rule.applyConditions : [],
    ruleDefinition: rule.ruleDefinition ?? {},
    accuracy: rule.accuracy ?? null,
    sensitivity: rule.sensitivity ?? null,
    specificity: rule.specificity ?? null,
    auc: rule.auc ?? null,
    consensusEligible: Boolean(rule.consensusEligible),
    isActive: Boolean(rule.isActive),
    sortOrder: Number(rule.sortOrder ?? 0),
    createdAt: rule.createdAt,
    updatedAt: rule.updatedAt,
  };
}

const snapshot = readSnapshot();
const version = snapshot.version;

if (!version) {
  throw new Error("Demo data snapshot is missing version.");
}
if (!Array.isArray(snapshot.outcomes) || !Array.isArray(snapshot.rules)) {
  throw new Error("Demo data snapshot must include outcomes and rules arrays.");
}

const outcomes = snapshot.outcomes.map(normalizeOutcome);
const rules = snapshot.rules.map(normalizeRule);

const source = `// Auto-generated by scripts/generate-demo-data.mjs. Do not edit by hand.

export const DEMO_DATA_VERSION = "${version}";

export const DEMO_OUTCOMES = ${JSON.stringify(outcomes, null, 2)} as const;

export const DEMO_RULES = ${JSON.stringify(rules, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
fs.writeFileSync(OUTPUT, source, "utf8");
console.log(
  `Generated ${path.relative(ROOT, OUTPUT)} with ${outcomes.length} outcomes and ${rules.length} rules from ${path.relative(ROOT, SNAPSHOT)}.`,
);

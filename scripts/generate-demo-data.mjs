const seedFiles = [
  "scripts/seed_literature.mjs",
  "scripts/seed_gdrive_literature.mjs",
];

function extractRulesFromSeed(file) {
  const abs = path.join(ROOT, file);
  const text = fs.readFileSync(abs, "utf8");
  const match = text.match(/const rules = \[([\s\S]*?)\n\];/);
  if (!match) {
    throw new Error(`Could not find rules array in ${file}`);
  }

  const code = `
    const outcomeId = 1;
    const walkOutcomeId = 1;
    const fimOutcomeId = 2;
    const communityOutcomeId = 3;
    const rules = [${match[1]}
    ];
    rules;
  `;

  return vm.runInNewContext(code, { JSON });
}

function parseJsonMaybe(value, fallback) {
  if (typeof value !== "string") return value ?? fallback;
  try {
    return JSON.parse(value);
  } catch {
    return fallback;
  }
}

const seen = new Set();
const rules = seedFiles
  .flatMap(extractRulesFromSeed)
  .filter((rule) => {
    if (seen.has(rule.name)) return false;
    seen.add(rule.name);
    return true;
  })
  .map((rule, index) => ({
    id: index + 1,
    outcomeId: rule.outcomeId,
    name: rule.name,
    ruleType: rule.ruleType,
    source: rule.source,
    sourceUrl: rule.sourceUrl ?? null,
    evidenceLevel: rule.evidenceLevel,
    applyConditions: parseJsonMaybe(rule.applyConditions, []),
    ruleDefinition: parseJsonMaybe(rule.ruleDefinition, {}),
    accuracy: rule.accuracy ?? null,
    sensitivity: rule.sensitivity ?? null,
    specificity: rule.specificity ?? null,
    auc: rule.auc ?? null,
    consensusEligible: Boolean(rule.consensusEligible),
    isActive: Boolean(rule.isActive),
    sortOrder: rule.sortOrder ?? index + 1,
    createdAt: GENERATED_AT,
    updatedAt: GENERATED_AT,
  }));

const source = `// Auto-generated by scripts/generate-demo-data.mjs. Do not edit by hand.

export const DEMO_DATA_VERSION = "${DEMO_DATA_VERSION}";

export const DEMO_OUTCOMES = ${JSON.stringify(outcomes, null, 2)} as const;

export const DEMO_RULES = ${JSON.stringify(rules, null, 2)} as const;
`;

fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
fs.writeFileSync(OUTPUT, source, "utf8");
